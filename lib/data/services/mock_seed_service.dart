  static List<Activity> activities(List<Lead> leads) {
    final now = DateTime.now();
    return leads
        .map(
          (lead) => Activity(
            id: _uuid.v4(),
            leadId: lead.id,
            type: 'lead_created',
            message: 'Lead created from ${lead.source}.',
            performedBy: lead.createdBy,
            createdAt: lead.createdAt,
          ),
        )
        .followedBy(
          [
            Activity(
              id: _uuid.v4(),
              leadId: leads.first.id,
              type: 'status_changed',
              message: 'Status updated to Interested.',
              performedBy: 'u_sales_1',
              createdAt: now.subtract(const Duration(hours: 3)),
            ),
            Activity(
              id: _uuid.v4(),
              leadId: leads.first.id,
              type: 'followup_scheduled',
              message: 'Follow-up scheduled for this evening.',
              performedBy: 'u_sales_1',
              createdAt: now.subtract(const Duration(hours: 2)),
            ),
          ],
        )
        .toList();
  }

  static List<FollowUp> followUps(List<Lead> leads) {
    final now = DateTime.now();
    return leads
        .where((e) => e.nextFollowUpAt != null)
        .map(
          (e) => FollowUp(
            id: _uuid.v4(),
            leadId: e.id,
            assignedTo: e.assignedTo,
            dueAt: e.nextFollowUpAt!,
            completed: false,
            lastNote: e.notesSummary,
          ),
        )
        .followedBy([
          FollowUp(
            id: _uuid.v4(),
            leadId: leads.first.id,
            assignedTo: 'u_sales_1',
            dueAt: now.subtract(const Duration(days: 1)),
            completed: true,
            lastNote: 'Customer requested revised proposal.',
          ),
        ])
        .toList();
  }
